// Two's complement is a mathematical operation used in computer science to represent negative numbers in binary form.
// In two's complement, the negative of a number is obtained by inverting all the bits of the number and adding 1 to the result.
// For example, to find the two's complement of the number 5 (which is 0000 0101 in binary), you would first invert the bits to get 
// 1111 1010, and then add 1 to get 1111 1011. This means that -5 is represented as 1111 1011 in two's complement form.
// In short, two's complement allows computers to perform arithmetic operations on both positive and negative numbers
